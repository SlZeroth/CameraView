package com.pjcstudio.supercamera.Model;

/**
 * Created by pjc on 2016. 11. 9..
 * This Class provide width and height
 */

public class Dimension {

    private int width;
    private int height;

    public Dimension(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
